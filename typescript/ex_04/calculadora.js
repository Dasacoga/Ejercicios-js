"use strict";
$(function () {
    $(".suma").click(function () {
        $("#resul").hide(); //Metodo qee esconde elemento seleccionado
    });
    $(".resta").click(function () {
        $("#resul").show(); //Método que muestra el elemento seleccionado
    });
    $(".multiplicacion").click(function () {
        $("#resul").hide(); //Metodo qee esconde elemento seleccionado
    });
    $(".division").click(function () {
        $("#resul").show(); //Método que muestra el elemento seleccionado
    });
});
